# Profundizando en los sistemas de numeracion

#SISTEMA DECIMAL
a = 10
print(f"a decimal: {a}")

#SISTEMA BINARIO
a = 0b1010 #10 en numero binario --> "0b"
print(f"a binario: {a}")

#SISTEMA OCTAL
a = 0o12 #10 en numero octal --> "0o"
print(f"a octal: {a}")

#SISTEMA HEXADECIMAL
a = 0xA #10 en sistema Hexadecimal --> "0x"
print(f"a Hexadecimal: {a}")

