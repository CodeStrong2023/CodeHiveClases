# Base Decimal
a = int('23', 10) #El 10 seria la base
print(f"a = base decimal: {a}")

#Base binario
a = int('10111', 2)
print(f"a = base binario: {a}")

#Base octal
a = int('27', 8)
print(f"a = base octal: {a}")

#Base Hexadecimal
a = int('17', 16)
print(f"a = base hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int('34', 5)
print(f"a = base 5: {a}")





